function testingSwitch() {
  var nilai = 1;
  switch (nilai) {
    case 10:
      console.log("Sangat bagus");
      break;
    case 8:
      console.log("Baik");
      break;
    case 6:
      console.log("Cukup");
      break;
    default:
      console.log("Belajar lagi");
  }
}

testingSwitch();
