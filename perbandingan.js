var x = 10;
var z = 11;

console.log(x > z); //lebih besar dari
console.log(x < z); // lebih kecil dari
console.log(x >= z); // lebih besar sama dengan
console.log(x <= z); // lebih kecil sama dengan
console.log(x == z); // sama dengan
console.log(x === z); // sama dengan type data harus dama
console.log(x != z); // tidak sama dengan
console.log(x !== z); // tidak sama dengan type data harus sama
