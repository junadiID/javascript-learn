// Metode trigonometri
Math.sin(x); // Mengembalikan sinus dari x (dalam radian)
Math.cos(x); // Mengembalikan kosinus dari x (dalam radian)
Math.tan(x); // Mengembalikan tangen dari x (dalam radian)
Math.asin(x); // Mengembalikan arcsinus dari x (dalam radian)
Math.acos(x); // Mengembalikan arccosinus dari x (dalam radian)
Math.atan(x); // Mengembalikan arctangen dari x (dalam radian)
Math.atan2(y, x); // Mengembalikan arctangen dari y / x (dalam radian)

// Metode logaritma
Math.log(x); // Mengembalikan logaritma natural dari x
Math.log10(x); // Mengembalikan logaritma basis 10 dari x
Math.log2(x); // Mengembalikan logaritma basis 2 dari x

// Metode eksponensial
Math.exp(x); // Mengembalikan eksponensial dari x

// Metode pembulatan
Math.round(x); // Mengembalikan x dibulatkan ke bilangan bulat terdekat
Math.floor(x); // Mengembalikan x dibulatkan ke bawah ke bilangan bulat terdekat
Math.ceil(x); // Mengembalikan x dibulatkan ke atas ke bilangan bulat terdekat

// Metode nilai mutlak dan sign
Math.abs(x); // Mengembalikan nilai mutlak dari x
Math.sign(x); // Mengembalikan tanda dari x: 1, -1, atau 0

// Metode akar kuadrat dan pangkat
Math.sqrt(x); // Mengembalikan akar kuadrat dari x
Math.pow(x, y); // Mengembalikan x pangkat y

// Metode random
Math.random(); // Mengembalikan angka acak antara 0 (inklusif) dan 1 (eksklusif)
